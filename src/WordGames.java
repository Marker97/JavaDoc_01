public class WordGames {
    /**
     * This method returns the concatenation of one strings as a parameter
     * @param word param to concat
     * @return concat String "Hello" + word
     */
    public String addHelloWord(String word){
        return "Hello " + word;
    }

    /**
     * This method returns the concatenation of two strings as a parameters
     * @param name first param
     * @param surname second param
     * @return concat name + surname
     */
    public String getFullName(String name, String surname){
        return name + " " + surname;
    }


}
